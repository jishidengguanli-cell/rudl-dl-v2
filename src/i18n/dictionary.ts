const makeDict = (overrides: Partial<Dict>): Dict => {
  const sanitizedEntries = Object.entries(overrides).filter(
    ([, value]) => typeof value === 'string'
  ) as Array<[keyof Dict, string]>;
  return { ...en, ...Object.fromEntries(sanitizedEntries) };
};

